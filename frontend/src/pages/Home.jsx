                           console.log('‚ùå Farcaster Ïù∏Ï¶ù Ïã§Ìå®:', error);
                           console.log('‚ö†Ô∏è ÏùºÎ∞ò Ïõπ Î∏åÎùºÏö∞Ï†ÄÏóêÏÑúÎäî Farcaster Ïù∏Ï¶ùÏù¥ Î∂àÍ∞ÄÎä•Ìï©ÎãàÎã§');
