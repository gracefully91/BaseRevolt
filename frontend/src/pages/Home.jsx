                           console.log('üîÑ Quick Auth Ïû¨ÏãúÎèÑ...');
                           const { token } = await sdk.quickAuth.getToken();
                           if (token) {
                             // ÌÜ†ÌÅ∞ÏùÑ ÏùºÏ£ºÏùºÍ∞Ñ Ï†ÄÏû•
                             const expiryDate = new Date(Date.now() + 7 * 24 * 60 * 60 * 1000); // 7Ïùº
                             localStorage.setItem('farcaster-token', token);
                             localStorage.setItem('farcaster-token-expiry', expiryDate.toISOString());
                             
                             setUser({ fid: 'authenticated', token });
                             console.log('‚úÖ Farcaster Ïù∏Ï¶ù ÏÑ±Í≥µ (7ÏùºÍ∞Ñ Ïú†ÏßÄ)');
                           }
