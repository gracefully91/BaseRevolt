          text: `üéÆ Check out Base Revolt! Control a real RC car remotely on Base blockchain!\n\n‚ú® Features:\n‚Ä¢ Real-time video streaming\n‚Ä¢ Remote RC car control\n‚Ä¢ Blockchain ownership proof\n\nTry it now: ${window.location.origin}`,
