    // Ï†ÄÏû•Îêú ÌÜ†ÌÅ∞ ÏÇ≠Ï†ú
    localStorage.removeItem('farcaster-token');
    localStorage.removeItem('farcaster-token-expiry');
    
    // ÏÇ¨Ïö©Ïûê ÏÉÅÌÉú Ï¥àÍ∏∞Ìôî
