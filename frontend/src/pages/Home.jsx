    const vehicle = vehicleManager.getVehicleById('car-001');
    if (!vehicle) return;

    // ÌòÑÏû¨ ÏÇ¨Ïö©Ïûê Ï†ïÎ≥¥ ÏóÖÎç∞Ïù¥Ìä∏
